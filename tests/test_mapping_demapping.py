    def perform_test(self):

        config = {
            "n_ul_rb": 100,
            "n_ra_prb_offset": 0,
            "phi": 7,
            "delta_f_ra": 1250,
            "delta_f": 15000
        }

        mapping = SubcarrierMappingBlock(config)
        demapping = SubcarrierDemappingBlock(config)

        # Mapping

        mapping_data = CommonDataEx()

        mapping_data.meta = {
            "dft": self.input_data,
            "n_ul_rb": config["n_ul_rb"],
            "n_ra_prb_offset": config["n_ra_prb_offset"]
        }

        mapping.process(mapping_data)

        mapped_spectrum = np.array(
            mapping_data.meta.get("Subcarrier_Mapping", []),
            dtype=complex
        )

        # Demapping

        demapping_data = CommonDataEx()

        demapping_data.meta = {
            "fft": mapped_spectrum,
            "n_ul_rb": config["n_ul_rb"],
            "n_ra_prb_offset": config["n_ra_prb_offset"]
        }

        demapping.process(demapping_data)

        output_data = np.array(
            demapping_data.meta.get("Subcarrier_Demapping", []),
            dtype=complex
        )

        tolerance = 1e-7

        error_vector = np.abs(
            output_data - self.input_data
        )

        max_error = np.max(error_vector)

        mismatch_count = np.sum(
            error_vector > tolerance
        )

        print(f"MAX ERROR: {max_error}")
        print(f"MISMATCH COUNT: {mismatch_count}")

        self.assertTrue(
            np.allclose(
                output_data,
                self.input_data,
                atol=tolerance
            ),
            "Mapping -> Demapping round-trip failed"
        )

    def test_mapping_demapping(self):

        self.perform_test()


